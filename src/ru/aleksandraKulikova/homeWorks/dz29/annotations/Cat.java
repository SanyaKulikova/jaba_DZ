package ru.aleksandraKulikova.homeWorks.dz29.annotations;

@Config(configFile = "config.properties")
public class Cat {
    @Required
    private String name;
    @Required
    private int age;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getAge() {
        return age;
    }

    public void setAge(int age) {
        this.age = age;
    }

    @Override
    public String toString() {
        return "Cat{" +
                "name='" + name + '\'' +
                ", age=" + age +
                '}';
    }
}
